    public boolean verifica_Dados_Pessoais() {
        if (nome.getText().isEmpty() || cpf.getText().isEmpty() || rg.getText().isEmpty() || data.getText().isEmpty() || (sexo.getSelectedIndex() == 0) || (tipo_Sanguineo.getSelectedIndex() == 0)
                || (convenio.getSelectedIndex() == 0) || endereço.getText().isEmpty() || cep.getText().isEmpty()) {
            return false;
        }
        return true;
    }

    public void tipoDeCadastramentoAvançar() {
        if (operation) {
            jPanel5.setVisible(false);
            jPanel8.setVisible(true);
        } else {
            jPanel5.setVisible(false);
            jPanel6.setVisible(true);
        }
    }

    public void tipoDeCadastramentoVoltar() {
        if (operation) {
            jPanel5.setVisible(true);
            jPanel8.setVisible(false);
        } else {
            jPanel16.setVisible(true);
            jPanel8.setVisible(false);
        }
    }

    public void registra() {

        try {
            if (escreve.escreve_CommonUsers(nome.getText(), cpf.getText(), rg.getText(), (String) sexo.getSelectedItem(), data.getText(), (String) tipo_Sanguineo.getSelectedItem(), (String) convenio.getSelectedItem(),
                    tel.getText(), cel.getText(), email.getText(), pNome_Contato.getText(), pParentesco_Contato.getText(),
                    pTel_Contato.getText(), pCel_Contato.getText(), pEmail_Contato.getText(), endereço.getText(), cep.getText(),
                    alergiasArray, doençasArray, medicamentosArray
            )) {
                JOptionPane.showMessageDialog(null, "Usuario cadastrado com sucesso");

            } else {
                JOptionPane.showMessageDialog(null, "Esse usuario já existe");
            }
        } catch (IOException ex) {
            Logger.getLogger(RegisterUserCommon.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    void preecheComDesconhecidoParenteContatos(String telefone, String celular, String emai) {
        String des = "Desconhecido";
        if (telefone.isEmpty()) {
            pTel_Contato.setText(des);
        }
        if (celular.isEmpty()) {
            pCel_Contato.setText(des);
        }
        if (emai.isEmpty()) {
            pEmail_Contato.setText(des);
        }
    }

    void preecheComDesconhecidoContatos(String telefone, String celular, String emai) {
        String des = "Desconhecido";
        if (telefone.isEmpty()) {
            tel.setText(des);
        }
        if (celular.isEmpty()) {
            cel.setText(des);
        }
        if (emai.isEmpty()) {
            email.setText(des);
        }
    }

    public void escrever(String password) throws IOException {
        escreve.escreveSenha(cpf.getText(), password, false);

    }


