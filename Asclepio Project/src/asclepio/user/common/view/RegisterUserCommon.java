    private void jButtonSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSalvarActionPerformed
        if (senha.getPassword().length <= 3) {
            JOptionPane.showMessageDialog(null, "Escreva uma senha com no minimo 4 digitos");

        } else {
            char pass[] = senha.getPassword();
            String password = String.copyValueOf(pass);
            char pass2[] = senha2.getPassword();
            String password2 = String.copyValueOf(pass2);

            if (password.equals(password2)) {
                try {
                    registra();
                    escrever(password);
                    this.dispose();
                } catch (IOException ex) {
                    Logger.getLogger(RegisterUserCommon.class.getName()).log(Level.SEVERE, null, ex);
                }
            } else {
                JOptionPane.showMessageDialog(null, "Escreva senha iguais");
            }
        }


    }//GEN-LAST:event_jButtonSalvarActionPerformed

