    public boolean senha() {
        if (senha.getPassword().length <= 3) {
            JOptionPane.showMessageDialog(null, "Escreva uma senha com no minimo 4 digitos");

        } else {
            char pass[] = senha.getPassword();
            String password = String.copyValueOf(pass);
            char pass2[] = senha2.getPassword();
            String password2 = String.copyValueOf(pass2);

            if (password.equals(password2)) {
                return true;
            } else {
                JOptionPane.showMessageDialog(null, "Escreva senha iguais");
            }

        }
        return false;
    }

    public boolean verificaPreechimento() {
        if (nome.getText().isEmpty() || cpf.getValue() == null || (sexo.getSelectedIndex() == 0) || (profissao.getSelectedIndex() == 0) || hospital.getText().isEmpty() || (senha.getPassword().length == 0) || (senha2.getPassword().length == 0)) {
            JOptionPane.showMessageDialog(null, "Preecha todos os dados");
            return false;

        }
        return true;
    }

    private void jButtonCadastraActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonCadastraActionPerformed

        WriterProfissional escrita = new WriterProfissional();
        try {
            char pass[] = senha.getPassword();
            String password = String.copyValueOf(pass);
            if (verificaPreechimento() && senha()) {
                escrita.escreve_EspecialUsers(nome.getText(), cpf.getText(), (String) sexo.getSelectedItem(), (String) profissao.getSelectedItem(), hospital.getText(), password);

                this.dispose();
            }

        } catch (IOException ex) {
            Logger.getLogger(RegisterUserProfissional.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_jButtonCadastraActionPerformed

